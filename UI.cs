        public static void Prompt(string a)
        {
            List<char> liste = new List<char>();

            foreach (char c in a)
            { liste.Add(c); }

            Random rng = new Random();

            foreach (var b in liste)
            {
                Console.Write(b);
                int y = rng.Next(0, 2);
                System.Threading.Thread.Sleep(y);
            }
        }
